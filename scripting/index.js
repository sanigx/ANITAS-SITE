function cSharpHoverEffect(){

    let c = document.getElementById('c-sharp');
    let icon = document.getElementById('csharp');

    icon.style.backgroundColor="black";
    icon.style.color="white";
}

function backToNormal(){
    let c = document.getElementById('c-sharp');
    let icon = document.getElementById('csharp');

    icon.style.backgroundColor="";
    icon.style.color=""; 
}